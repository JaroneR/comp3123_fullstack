module.exports = {
    Add: function(num1, num2){
        let sum = num1 + num2;
        console.log("The sum of "+num1+" and "+num2+" is "+sum);
        return sum;
    },

    Subtract(num1, num2){
        let total = num1 - num2;
        console.log("The subtraction of "+num1+" and "+num2+" is "+total);
        return total;
    }

}